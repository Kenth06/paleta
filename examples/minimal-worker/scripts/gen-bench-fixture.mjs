#!/usr/bin/env node
// Inlines a fixture JPEG as base64 into src/bench-fixture.ts for the /bench endpoint.
import { readFile, writeFile } from "node:fs/promises";

const src = "../../../test/fixtures/paleta-scene-420.jpg";
const bytes = await readFile(new URL(src, import.meta.url));
const b64 = bytes.toString("base64");

const out = `// Auto-generated by scripts/gen-bench-fixture.mjs. Do not edit by hand.
// Source: ${src}
// Size: ${bytes.length} bytes raw, ${b64.length} chars base64.

export const BENCH_FIXTURE_BASE64 =
  ${JSON.stringify(b64)};

export function benchFixtureBytes(): Uint8Array {
  const bin = atob(BENCH_FIXTURE_BASE64);
  const out = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i);
  return out;
}
`;
await writeFile(new URL("../src/bench-fixture.ts", import.meta.url), out);
console.log(`wrote src/bench-fixture.ts (${bytes.length} bytes fixture)`);
